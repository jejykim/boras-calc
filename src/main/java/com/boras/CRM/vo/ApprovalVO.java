package com.boras.CRM.vo;

public class ApprovalVO {
	private int approvalLedgerSeq;
	private int approvalSeq;
	private String approvalUserId;
	private String approvalState;
	private String approvalYn;
	private String approvalRequestDate;
	private String approvalDate;
	
	public int getApprovalLedgerSeq() {
		return approvalLedgerSeq;
	}
	public void setApprovalLedgerSeq(int approvalLedgerSeq) {
		this.approvalLedgerSeq = approvalLedgerSeq;
	}
	public int getApprovalSeq() {
		return approvalSeq;
	}
	public void setApprovalSeq(int approvalSeq) {
		this.approvalSeq = approvalSeq;
	}
	public String getApprovalUserId() {
		return approvalUserId;
	}
	public void setApprovalUserId(String approvalUserId) {
		this.approvalUserId = approvalUserId;
	}
	public String getApprovalState() {
		return approvalState;
	}
	public void setApprovalState(String approvalState) {
		this.approvalState = approvalState;
	}
	public String getApprovalYn() {
		return approvalYn;
	}
	public void setApprovalYn(String approvalYn) {
		this.approvalYn = approvalYn;
	}
	public String getApprovalRequestDate() {
		return approvalRequestDate;
	}
	public void setApprovalRequestDate(String approvalRequestDate) {
		this.approvalRequestDate = approvalRequestDate;
	}
	public String getApprovalDate() {
		return approvalDate;
	}
	public void setApprovalDate(String approvalDate) {
		this.approvalDate = approvalDate;
	}
	
}
